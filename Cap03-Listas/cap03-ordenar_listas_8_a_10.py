mundo.reverse() #Invertendo lista
print(mundo)
mundo.reverse() #invertendo lista invertida, voltando a ordem original
print(mundo)

# Ordem alfabética
mundo.sort() #Ordem alfabética
print(mundo)
mundo.sort(reverse=True) #Ordem alfabética inversa
print(mundo)

