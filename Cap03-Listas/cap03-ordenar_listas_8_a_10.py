mundo = ['cuba','russia','japao','mexico','argentina']
print(mundo) #Ordem original
print(sorted(mundo)) #Ordem alfabética temporaria
print(mundo) #Ordem oringal
print(sorted(mundo,reverse=True)) #Ordem alfabética inversa temporaria
print(mundo) #Ordem oringal

